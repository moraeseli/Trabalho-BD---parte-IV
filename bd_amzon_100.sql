    ID_Ass INT NOT NULL,
    Prec_Ass FLOAT,
    Free_Frete BOOLEAN DEFAULT FALSE,
    Nome_Ass VARCHAR(50),
    CONSTRAINT PK_ASSINATURA PRIMARY KEY (ID_Ass)
