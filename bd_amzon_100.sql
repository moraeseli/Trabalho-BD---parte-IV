CREATE TABLE IF NOT EXISTS LOJA (
    ID_Loja INT NOT NULL,
    Nome_Loja VARCHAR(50),
    Email_Loja VARCHAR(50) UNIQUE,
    CPF_CNPJ_Loja VARCHAR(20) UNIQUE,
    Reput_Loja FLOAT,
    Data_Criac_Loja DATE,
    CONSTRAINT PK_LOJA PRIMARY KEY (ID_Loja)
